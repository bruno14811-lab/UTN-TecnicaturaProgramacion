# operadores de comparacion

d = 4
b = 2
resultado = d == b # comprobamos si son iguales
print(resultado)

# operador diferente
resultado = d != b
print(resultado)

# operador mayor que
resultado = d > b
print(resultado)

# operador menor que
resultado = d < b
print(resultado)

# operador menor o igual que
resultado = d <= b
print(resultado)

# operador mayor o igual que
resultado = d >= b
print(resultado)



